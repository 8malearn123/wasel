const ATTRS = ["placeholder", "title", "aria-label", "alt"] as const;

function translateTextNode(node: Text) {
  const raw = node.nodeValue;
  if (!raw) return;
  const trimmed = raw.trim();
  if (!trimmed) return;
  const hit = AR_EN[trimmed];
  if (hit) {
    node.nodeValue = raw.replace(trimmed, hit);
  } else if (raw.includes("ر.س")) {
    node.nodeValue = raw.split("ر.س").join("SAR");
  }
}

function translateElementAttrs(el: Element) {
  if (el.tagName === "SCRIPT" || el.tagName === "STYLE") return;
  for (const attr of ATTRS) {
    const v = el.getAttribute(attr);
    if (!v) continue;
    const hit = AR_EN[v.trim()];
    if (hit) el.setAttribute(attr, hit);
  }
}

function walk(root: Node) {
  if (root.nodeType === Node.TEXT_NODE) {
    translateTextNode(root as Text);
    return;
  }
  if (root.nodeType !== Node.ELEMENT_NODE && root.nodeType !== Node.DOCUMENT_FRAGMENT_NODE) return;
  if (root.nodeType === Node.ELEMENT_NODE) {
    const el = root as Element;
    if (el.tagName === "SCRIPT" || el.tagName === "STYLE") return;
    translateElementAttrs(el);
  }
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT);
  let n: Node | null;
  while ((n = walker.nextNode())) {
    if (n.nodeType === Node.TEXT_NODE) translateTextNode(n as Text);
    else translateElementAttrs(n as Element);
  }
}

let observer: MutationObserver | null = null;

export function startAutoTranslate() {
  if (observer) return;
  walk(document.body);
  observer = new MutationObserver((mutations) => {
    for (const m of mutations) {
      if (m.type === "characterData" && m.target.nodeType === Node.TEXT_NODE) {
        translateTextNode(m.target as Text);
      }
      m.addedNodes.forEach((n) => walk(n));
    }
  });
  observer.observe(document.body, { childList: true, subtree: true, characterData: true });
}

export function stopAutoTranslate() {
  observer?.disconnect();
  observer = null;
}
